task moveBase
	{

		SensorValue(baseL) = 0;
		SensorValue(baseR) = 0;

		const float k = 0.50;
		const int encDif = 2;
		int Q = (SensorValue(baseL)-SensorValue(baseR));

		while(SensorValue(baseL) < baseTarget){ // Andres Comment - Target based only on left

			if(Q <= encDif && Q >=-encDif)
			{
				motor[rbBase] = baseSpeed;
				motor[rfBase] = baseSpeed;
				motor[lbBase] = baseSpeed;
				motor[lfBase] = baseSpeed;
			}
			else if(Q > encDif)
			{
				if(baseSpeed >= 0){
					motor[rbBase] = baseSpeed;
					motor[rfBase] = baseSpeed;
					motor[lbBase] = baseSpeed*k;
					motor[lfBase] = baseSpeed*k;
				}
				else{
					motor[rbBase] = baseSpeed*k;
					motor[rfBase] = baseSpeed*k;
					motor[lbBase] = baseSpeed;
					motor[lfBase] = baseSpeed;

				}
			}
			//moving back
			else
			{
				if(baseSpeed >= 0){
					motor[rbBase] = baseSpeed*k;
					motor[rfBase] = baseSpeed*k;
					motor[lbBase] = baseSpeed;
					motor[lfBase] = baseSpeed;
				}
				else{
					motor[rbBase] = baseSpeed;
					motor[rfBase] = baseSpeed;
					motor[lbBase] = baseSpeed*k;
					motor[lfBase] = baseSpeed*k;
				}
			}

			baseStat = SensorValue(encL) / baseTarget;
	}
	motor[rbBase] = 0;
	motor[rfBase] = 0;
	motor[lbBase] = 0;
	motor[lfBase] = 0;
	}


