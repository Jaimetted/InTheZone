void armX(float speed)
{
	const float k = 0.50;
	const int encDif = 2;
	int Q = (SensorValue(encL)-SensorValue(encR)); // Andres Comment - Is sensor drift a problem?

	if(Q <= encDif && Q >=-encDif)
	{
		arm(speed);
	}
	else if(Q > encDif)
	{
		if(speed >= 0)
			arm(k*speed,speed);
		else
			arm(speed,k*speed);
	}
	else
	{
		if(speed >= 0)
			arm(speed,k*speed);
		else
			arm(k*speed,speed);
	}
}

